package agh.ics.oop.model;

/**
 * Enum representing the four possible movement directions.
 *
 * This enum defines the basic movement directions for entities in the system.
 */
public enum MoveDirection {
    FORWARD,   // Represents moving forward.
    BACKWARD,  // Represents moving backward.
    LEFT,      // Represents turning or moving left.
    RIGHT      // Represents turning or moving right.
}